

public class Agenda {
	public String Nome;
	public String Endereco;
	public String Email;
	
	public void setNome(String r) {
		Nome = r;
	}
	
	public void setEmail(String r) {
		this.Email = r;
	}
	
	public void setEndereco(String r) {
		this.Endereco = r;
	}
	
	public String getNome() {
		return this.Nome;
	}
	
	public String getEndereco() {
		return this.Endereco;
	}
	
	public String getEmail(){
		return this.Email;
	}
	
	
}



